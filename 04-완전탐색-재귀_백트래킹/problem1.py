# N 숫자의 범위, M 반복문의 횟수
def recur(n):
    if n == M:
        print(*arr)
        return
    for i in range(1, N+1):
        # - 1부터 N까지의 자연수 중에서 M개를 고른 수열
        # - 1부터 N까지의 자연수 중에서 중복 없이 M개를 고른 수열
        if i in arr: continue
        arr.append(i)
        # - 1부터 N까지의 자연수 중에서 중복 없이 오름차순으로 M개를 고른 수열
        # - 1부터 N까지의 자연수 중에서 중복 없이 내림차순으로 M개를 고른 수열
        recur(n+1)
        arr.pop()

arr = []
recur(0)