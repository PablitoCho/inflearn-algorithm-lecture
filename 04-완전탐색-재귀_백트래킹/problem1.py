# 1 3
# 1 4
# 2 2
# 2 3
# 2 4
# 3 3
# 3 4
# 4 4

N, M = map(int, input().split())
# print(f"N : {N}, M : {M}")

# 반복문 두 번 쓰면 된다. (완전탐색)
# for i in range(1, N+1):
